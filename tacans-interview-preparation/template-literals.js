// What are template literals in JavaScript? Provide an example of how they can be used.

// Template literals are string literals that allow embedded expressions. You can use multi-line strings and string interpolation features with them. They are enclosed by the back-tick (` `) (grave accent) character instead of double or single quotes.

// Example:

const name = "John";

// Using template literal
const message = `Hello, ${name}!`;

console.log(message); // Output: Hello, John!
